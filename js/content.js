function image_link() {
  let img = document.getElementsByTagName("img");

  for (let i = 0; i < img.length; i++) {
    let sr = img[i].src;
    if (sr.includes("drive.google.com/file")) {
      sr = sr.replace("file/d/", "uc?id=");
      sr = sr.replace("/view?usp=sharing", "");
      // console.log(sr);
      img[i].src = sr;
    }
  }
}

window.onload = function () {
  image_link();
};



